    public void listar(){
        ConsultaDao dao = new ConsultaDao();
        List<Consulta> lista = dao.listarConsulta();
        DefaultTableModel dados = (DefaultTableModel) tbconsultaa.getModel();
        dados.setNumRows(0);
        
        for (Consulta c : lista){
            dados.addRow(new Object[]{
                c.getId(),    
                c.getPaciente(),
                c.getNomep(),
                c.getObs(),
                c.getProfissionalsaude(),
                c.getValor(),
                c.getDataconsulta(),
                c.getTurnoconsulta()
            });
        }
    }

